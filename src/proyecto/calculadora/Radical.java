    private void BotonIgual1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonIgual1ActionPerformed
        // TODO add your handling code here:
        try{
            if(TextOperacion2.getText().contains("^")){
                String ResultadoFinal, auxDato1, auxDato2;
                auxDato1 = Dato1.replace("^", "");
                Dato1 = auxDato1;
                auxDato2 = TextOperacion2.getText().replace(Dato1, "");
                Dato2 = auxDato2.replace("^", "");
                if(!Dato2.equals("")){
                    ResultadoFinal = Exponente(Dato1,Dato2,Operador);
                    ResultadoExp = ResultadoFinal;
                    TextResultado2.setText(ResultadoFinal);
                    BotonError.setVisible(false);
                }
                else{
                    TextResultado2.setText("Error...");
                    BotonError.setVisible(true);
                }
                
                String auxOtraOpera1 = TextOperacion2.getText().replace(Dato1, "");
                String auxOtraOpera2 = auxOtraOpera1.replace("^", "");
                String OtraOpera =  auxOtraOpera2.replace(Dato2, ResultadoExp);
                TextOperacion2.setText(OtraOpera);
                String Resultados = SE.eval(TextOperacion2.getText()).toString();
                TextResultado2.setText(Resultados);
                BotonError.setVisible(false);
            }
            else{
                String Resultado = SE.eval(TextOperacion2.getText()).toString();
                TextResultado2.setText(Resultado);
                BotonError.setVisible(false); 
            }
        }catch (Exception e){
            TextResultado2.setText("Error...");
            BotonError.setVisible(true);
        }
    }//GEN-LAST:event_BotonIgual1ActionPerformed

