    public static String Exponente(String Dato1, String Dato2, String Operador){
        Double ResultadoF = 0.0;
        String RespuestaF;
        String Dato11 = Dato1;
        String Dato22 = Dato2;
        if(Operador.equals("^")){
            ResultadoF = Math.pow(Double.parseDouble(Dato11),Double.parseDouble(Dato22));
        }
        RespuestaF = ResultadoF.toString();
        return RespuestaF;   
    }

